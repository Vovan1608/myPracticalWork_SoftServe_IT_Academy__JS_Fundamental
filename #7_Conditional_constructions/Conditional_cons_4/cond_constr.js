var grade = Number(prompt("Enter the integer number.",""));
switch(grade){
    case 1:
        console.log("badly");
        break;
    case 2:
        console.log("unsatisfactory");
        break;
    case 3:
        console.log("satisfactory");
        break;
    case 4:
        console.log("good");
        break;
    case 5:
        console.log("perfectly");
        break;
    default:
        console.log("Error");
}
